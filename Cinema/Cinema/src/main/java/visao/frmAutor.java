    private void btSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btSalvarMouseClicked
       Autor obj = new Autor();
        
        obj.setNome(txtNome.getText());
        obj.setNacionalidade(txtNacionalidade.getText());
        obj.setSexo(cbSexo.getSelectedItem().toString());
        obj.setIdade(txtIdade.getText());
        
        AutorDAO dao = new AutorDAO();
        dao.cadastrarAutor(obj);
         new Utilitarios().LimparTela(painel_dados);
    }//GEN-LAST:event_btSalvarMouseClicked

    private void btEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btEditarMouseClicked
      Autor obj = new Autor();
        
        obj.setNome(txtNome.getText());
        obj.setNacionalidade(txtNacionalidade.getText());
        obj.setSexo(cbSexo.getSelectedItem().toString());
        obj.setIdade(txtIdade.getText());
       
        
        obj.setCodAutor(Integer.parseInt(txtCodAutor.getText()));
        
        AutorDAO dao = new AutorDAO();
        dao.alterarAutor(obj);
        new Utilitarios().LimparTela(painel_dados);
                         
    }//GEN-LAST:event_btEditarMouseClicked

    private void btExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btExcluirMouseClicked
        Autor obj = new Autor();
        
        obj.setCodAutor(Integer.parseInt(txtCodAutor.getText()));
        
        AutorDAO dao =new AutorDAO();
        
        dao.excluirAutor(obj);
         new Utilitarios().LimparTela(painel_dados);
                                         
    }//GEN-LAST:event_btExcluirMouseClicked

    private void tbFilmesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbFilmesMouseClicked
       tbFilme.setSelectedIndex(0);
        
        txtCodAutor.setText(tbFilmes.getValueAt(tbFilmes.getSelectedRow(),0).toString());
        txtNome.setText(tbFilmes.getValueAt(tbFilmes.getSelectedRow(),1).toString());
        txtNacionalidade.setText(tbFilmes.getValueAt(tbFilmes.getSelectedRow(),5).toString());
        cbSexo.setSelectedItem(tbFilmes.getValueAt(tbFilmes.getSelectedRow(),2).toString());
        txtIdade.setText(tbFilmes.getValueAt(tbFilmes.getSelectedRow(),3).toString());
    
                                      
    }//GEN-LAST:event_tbFilmesMouseClicked

    private void btPesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btPesquisarMouseClicked
       String nome = "%" + txtPesquisar.getText()+ "%";
        AutorDAO dao = new AutorDAO();
        List<Autor> lista = dao.buscaCinemaPorNome(nome);

        DefaultTableModel dados = (DefaultTableModel) tbFilmes.getModel();
        dados.setNumRows(0);
        
        for (Autor c: lista){
            dados.addRow(new Object[]{
                c.getCodAutor(),
                c.getNome(),
                c.getNacionalidade(),
                c.getSexo(),
                c.getIdade(),
              
            });
                 
    }                    
    }//GEN-LAST:event_btPesquisarMouseClicked

