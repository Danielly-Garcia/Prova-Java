    private void btSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btSalvarMouseClicked
       Participantes obj = new Participantes();
        
        obj.setPersonagem(txtPersonagem.getText());
        obj.setNome(txtNome.getText());
        obj.setTituloOriginal(txtTituloOriginal.getText());
        
        
        Autor a= new Autor();
        a = (Autor) cbAutor.getSelectedItem();
        
        obj.setAutor(a);
        
        
        Filmes f= new Filmes();
        f = (Filmes) cbFilme.getSelectedItem();
        
        obj.setFilme(f);
        
        ParticipanteDAO dao = new ParticipanteDAO();
        dao.cadastrarParticipante(obj);
         new Utilitarios().LimparTela(painel_dados);
    }//GEN-LAST:event_btSalvarMouseClicked

    private void btEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btEditarMouseClicked
       Participantes obj = new Participantes();
        
        obj.setPersonagem(txtPersonagem.getText());
        obj.setNome(txtNome.getText());
        obj.setTituloOriginal(txtTituloOriginal.getText());
        
        obj.setCodParticipanteFilme(Integer.parseInt(txtCodParticipanteFilme.getText()));
        
        ParticipanteDAO dao = new ParticipanteDAO();
        dao.alterarParticipante(obj);
        new Utilitarios().LimparTela(painel_dados);
                         
    }//GEN-LAST:event_btEditarMouseClicked

    private void btExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btExcluirMouseClicked
        Participantes obj = new Participantes();
        
        obj.setCodParticipanteFilme(Integer.parseInt(txtCodParticipanteFilme.getText()));
        
        ParticipanteDAO dao =new ParticipanteDAO();
        
        dao.excluirParticipante(obj);
         new Utilitarios().LimparTela(painel_dados);
                                         
    }//GEN-LAST:event_btExcluirMouseClicked

    private void tbFilmesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbFilmesMouseClicked
       tbFilme.setSelectedIndex(0);
        
        txtCodParticipanteFilme.setText(tbFilmes.getValueAt(tbFilmes.getSelectedRow(),0).toString());
        txtPersonagem.setText(tbFilmes.getValueAt(tbFilmes.getSelectedRow(),1).toString());
        txtNome.setText(tbFilmes.getValueAt(tbFilmes.getSelectedRow(),5).toString());
        txtTituloOriginal.setText(tbFilmes.getValueAt(tbFilmes.getSelectedRow(),6).toString());
       
                                      
    }//GEN-LAST:event_tbFilmesMouseClicked

